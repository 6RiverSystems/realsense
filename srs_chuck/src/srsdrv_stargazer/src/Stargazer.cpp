Stargazer::Stargazer() :
    rosNodeHandle_()
{

}

////////////////////////////////////////////////////////////////////////////////////////////////////
void Stargazer::run()
{
    ros::Rate refreshRate(REFRESH_RATE_HZ);

    while (ros::ok())
    {
        ros::spinOnce();

        refreshRate.sleep();
    }
}

