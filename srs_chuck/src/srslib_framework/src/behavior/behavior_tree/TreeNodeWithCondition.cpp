template<class CONTEXT>
typename TreeNode<CONTEXT>::NodeResult TreeNodeWithCondition<CONTEXT>::execute(CONTEXT* context)
{
    if (preCondition_)
    {
        return preCondition_->execute(context);
    }

    return TreeNode<CONTEXT>::NodeResult::SUCCEDED;
}

