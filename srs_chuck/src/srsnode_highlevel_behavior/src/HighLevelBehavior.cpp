HighLevelBehavior::HighLevelBehavior() :
    rosNodeHandle_()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void HighLevelBehavior::run()
{
    ros::Rate refreshRate(REFRESH_RATE_HZ);
    while (ros::ok())
    {
        ros::spinOnce();

        refreshRate.sleep();
    }
}

