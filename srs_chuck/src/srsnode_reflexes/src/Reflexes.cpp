Reflexes::Reflexes() :
    rosNodeHandle_()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void Reflexes::run()
{
    ros::Rate refreshRate(REFRESH_RATE_HZ);
    while (ros::ok())
    {
        ros::spinOnce();

        refreshRate.sleep();
    }
}

